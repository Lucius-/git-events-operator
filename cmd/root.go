package cmd

import (
	"os"

	"github.com/heptiolabs/git-events-operator/operator"
	"github.com/kubicorn/kubicorn/pkg/logger"
	"github.com/spf13/cobra"
)

var cfgFile string

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "git-events-operator",
	Short: "Run the operator",
	Long:  `Most of the operator configuration can be found in config.go for this initial release`,
	Run: func(cmd *cobra.Command, args []string) {
		err := operator.Reconcile(operatorConfig)
		if err != nil {
			logger.Critical("Fatal error during reconcile: %v", err)
			os.Exit(1)
		}
		logger.Success("Success!")
		os.Exit(0)
	},
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	rootCmd.Execute()
}

